class StringExample {
    static String a = "Hello";

    public static void main(String[] args) {
        String b;
        String c = "Hari";
        System.out.println("My name is: " + c);
        b = "World";
        System.out.println(a);
        System.out.println(b);
        System.out.println("String data type example");
        b = "Java";
        System.out.println("Updated string value: " + b);
    }
}
