class CharExample {
    static char a = 'H';

    public static void main(String[] args) {
        char b;
        String c = "Hari";
        System.out.println("My name is: " + c);
        b = 'A';
        System.out.println(a);
        System.out.println(b);
        System.out.println("Char data type example");
        b = 'Z';
        System.out.println("Updated char value: " + b);
    }
}
