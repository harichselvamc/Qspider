class LongExample {
    static long a = 100000L;

    public static void main(String[] args) {
        long b;
        String c = "Hari";
        System.out.println("My name is: " + c);
        b = 200000L;
        System.out.println(a);
        System.out.println(b);
        System.out.println("Long data type example");
        b = 300000L;
        System.out.println("Updated long value: " + b);
    }
}
