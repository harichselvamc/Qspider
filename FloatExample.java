class FloatExample {
    static float a = 10.5f;

    public static void main(String[] args) {
        float b;
        String c = "Hari";
        System.out.println("My name is: " + c);
        b = 20.5f;
        System.out.println(a);
        System.out.println(b);
        System.out.println("Float data type example");
        b = 30.5f;
        System.out.println("Updated float value: " + b);
    }
}
