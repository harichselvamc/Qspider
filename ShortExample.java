class ShortExample {
    static short a = 1000;

    public static void main(String[] args) {
        short b;
        String c = "Hari";
        System.out.println("My name is: " + c);
        b = 2000;
        System.out.println(a);
        System.out.println(b);
        System.out.println("Short data type example");
        b = 3000;
        System.out.println("Updated short value: " + b);
    }
}
